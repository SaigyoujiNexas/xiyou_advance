public class News_Homepage {
    int img_newsHomepage;
    String title_newsHomepage;

    public News_Homepage(int img_newsHomepage, String title_newsHomepage) {
        this.img_newsHomepage = img_newsHomepage;
        this.title_newsHomepage = title_newsHomepage;
    }

    public int getImg_newsHomepage() {
        return img_newsHomepage;
    }

    public void setImg_newsHomepage(int img_newsHomepage) {
        this.img_newsHomepage = img_newsHomepage;
    }

    public String getTitle_newsHomepage() {
        return title_newsHomepage;
    }

    public void setTitle_newsHomepage(String title_newsHomepage) {
        this.title_newsHomepage = title_newsHomepage;
    }
}
